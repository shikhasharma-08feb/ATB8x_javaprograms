package SEPT.Ex_25092024;

public class Lab049_Print_Even_No_till_50 {
    public static void main(String[] args) {
        for (int i = 1; i <=50 ; i++) {
            if (i%2==0)
            {
                System.out.println("Even No "+i);
            }
            else
            {
                System.out.println("Odd No" +i);
            }
        }
    }
}
