package SEPT.Ex_13092024;

public class Lab011_PrintCalc {
    public static void main(String[] args) {
        double a=34, b=10;

        System.out.printf("%f+%f=%f\n",a,b,(a+b));
        System.out.printf("%f+%f=%f\n",a,b,(a-b));
        System.out.printf("%f+%f=%f\n",a,b,(a*b));
        System.out.printf("%f+%f=%f\n",a,b,(a/b));
        System.out.printf("%f+%f=%f\n",a,b,(a%b));


    }

}
