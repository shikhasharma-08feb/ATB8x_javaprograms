public class Lab021_QnA {
    public static void main(String[] args) {
        int a=10;
        int b=10;
        System.out.println(a==b); //10==10 //true
        System.out.println(a>=b); // 10>=10 //true
        System.out.println(a<=b); //10<=10 //true
        System.out.println(a>b);  //10>10 //False
        System.out.println(a<b); //10<10 //False

    }
}
