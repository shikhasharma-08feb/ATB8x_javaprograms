package SEPT.Ex_16092024;

public class Lab020_LogicalOperator {
    public static void main(String[] args) {
        //gates - &&(and), || Or and ! Not
        boolean a=true;
        boolean b=false;
        boolean c= a&&b;
        boolean d=a||b;
        boolean e=!a;
        boolean f=!b;
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);

    }
}
