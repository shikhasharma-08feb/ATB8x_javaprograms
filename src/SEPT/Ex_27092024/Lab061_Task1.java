package SEPT.Ex_27092024;

public class Lab061_Task1 {
    public static void main(String[] args) {
        //Give me example of while vs do while loop in Lab ex.

        int a=10;
        while(a<=10)
        {
            System.out.println(a);
            a++;
        }
       do {
           System.out.println(a);
           a++;
       }while(a<10);
    }
}
